def create_comparison_data():
    """创建对比数据"""
    df_stock = pd.DataFrame(WENS_STOCK_DATA, columns=['date', 'stock_price'])
    df_pig = pd.DataFrame(PIG_PRICE_DATA, columns=['date', 'pig_price'])
    
    # 合并数据
    df = pd.merge(df_stock, df_pig, on='date', how='outer')
    df = df.sort_values('date').reset_index(drop=True)
    
    return df


def calculate_correlation(df):
    """计算相关性"""
    # 去除空值
    df_clean = df.dropna()
    
    if len(df_clean) < 10:
        return 0
    
    # 计算相关系数
    correlation = df_clean['stock_price'].corr(df_clean['pig_price'])
    
    return correlation


def generate_chart_data():
    """生成图表数据 (用于ECharts)"""
    df = create_comparison_data()
    
    # 标准化数据 (用于对比)
    df['stock_price_norm'] = (df['stock_price'] - df['stock_price'].min()) / (df['stock_price'].max() - df['stock_price'].min()) * 40
    df['pig_price_norm'] = df['pig_price']
    
    chart_data = {
        'dates': df['date'].tolist(),
        'stock_price': df['stock_price'].tolist(),
        'pig_price': df['pig_price'].tolist(),
        'stock_price_norm': df['stock_price_norm'].tolist(),
    }
    
    return chart_data


def generate_analysis_summary():
    """生成分析摘要"""
    df = create_comparison_data()
    correlation = calculate_correlation(df)
    
    # 计算收益率
    df['stock_return'] = df['stock_price'].pct_change() * 100
    df['pig_return'] = df['pig_price'].pct_change() * 100
    
    summary = {
        'total_months': len(df),
        'correlation': round(correlation, 3),
        'stock_max': df['stock_price'].max(),
        'stock_min': df['stock_price'].min(),
        'stock_current': df['stock_price'].iloc[-1],
        'pig_max': df['pig_price'].max(),
        'pig_min': df['pig_price'].min(),
        'pig_current': df['pig_price'].iloc[-1],
        'stock_avg_return': round(df['stock_return'].mean(), 2),
        'pig_avg_return': round(df['pig_return'].mean(), 2),
    }
    
    return summary


